#include <iostream>
using namespace std;

int main() {
  int x, y;
  int sum;
  cout << "Số thứ nhất: "; // Khai báo biến
  cin >> x; // Nhận thông tin nhập của người dùng từ bàn phím
  cout << "Số thứ hai: "; // Khai báo biến
  cin >> y; // Nhận thông tin nhập của người dùng từ bàn phím
  sum = x + y; // tổng biến thứ nhất và biến thứ 2
  cout << "tổng hai số: " << sum; // Gọi biến
  return 0;
}
